public class Medidor {
    private double passivo;
    private double assertivo;
    private double agressivo;

    public double getPassivo() {
        return passivo;
    }

    public void setPassivo(double passivo) {
        this.passivo = passivo;
    }

    public double getAssertivo() {
        return assertivo;
    }

    public void setAssertivo(double assertivo) {
        this.assertivo = assertivo;
    }

    public double getAgressivo() {
        return agressivo;
    }

    public void setAgressivo(double agressivo) {
        this.agressivo = agressivo;
    }
}
